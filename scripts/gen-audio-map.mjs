#!/usr/bin/env node
// assets/audio/{words,examples}/<id>.mp3 → src/lib/audio/audioMap.gen.ts
// id -> require(mp3) 정적 맵. Metro가 mp3를 번들 에셋으로 포함하게 한다.
//
// 실행: node scripts/gen-audio-map.mjs
// 선행: scripts/gen-tts-audio.py 로 mp3 생성 완료

import { readdirSync, writeFileSync, existsSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const BASE = resolve(ROOT, 'assets/audio');
const OUT = resolve(ROOT, 'src/lib/audio/audioMap.gen.ts');

function entries(kindDir) {
  const dir = resolve(BASE, kindDir);
  if (!existsSync(dir)) return [];
  return readdirSync(dir)
    .filter((f) => f.endsWith('.mp3'))
    .map((f) => {
      const id = f.slice(0, -4);
      return `  ${JSON.stringify(id)}: require('../../../assets/audio/${kindDir}/${f}'),`;
    });
}

const words = entries('words');
const examples = entries('examples');

const out = `// AUTO-GENERATED by scripts/gen-audio-map.mjs — do not edit by hand.
// id -> bundled mp3 asset module. Used by src/lib/audio/wordAudio.ts.
/* eslint-disable */
export const WORD_AUDIO: Record<string, number> = {
${words.join('\n')}
};
export const EXAMPLE_AUDIO: Record<string, number> = {
${examples.join('\n')}
};
`;

writeFileSync(OUT, out);
console.log(`generated ${OUT}: words=${words.length} examples=${examples.length}`);
